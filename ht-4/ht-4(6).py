#Вводиться число. Якщо це число додатне, знайти його квадрат, якщо від'ємне, збільшити його на 100, якщо дорівнює 0, не змінювати


n = int(input('Введіть число n: '))     

if n > 0:                              
    print(n**2)
elif n < 0:
    print(n+100)
elif n==0:
    print(0)

 
 

